/// <summary>
/// 隐藏属性
/// </summary>
public enum HideAttributeType : byte
{
    /// <summary>
    /// 最终伤害值
    /// </summary>
    FinalDamage,

    /// <summary>
    /// 最终减免值
    /// </summary>
    FinalDamgeCounteract,

    /// <summary>
    /// 最终伤害加成
    /// </summary>
    FinalDamageAddition,

    /// <summary>
    /// 最终减免加成
    /// </summary>
    FinalDamageCounteractAddition,

    /// <summary>
    /// 物理伤害加成
    /// </summary>
    PhysicsDamageAddition,

    /// <summary>
    /// 物理减免加成
    /// </summary>
    PhysicsDamageCounteractAddition,

    /// <summary>
    /// 法术伤害加成
    /// </summary>
    MagicDamageAddition,

    /// <summary>
    /// 法术减免加成
    /// </summary>
    MagicDamageCounteractAddition,

}
